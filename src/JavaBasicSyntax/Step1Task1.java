package JavaBasicSyntax;

public class Step1Task1 {
    public static void main(String[] args) {
        byte byteVar = -128;
        short shortVar = 32767;
        int intVar = 2147483647;
        float floatVar = 4.12f;
        double doubleVar = 213.2342;
        char charVar = '$';
        boolean boolVar = false;
        String stringVar = "some string";

        System.out.print("byteVar = " + byteVar + ", " + "shortVar = " + shortVar + ", " + "intVar = " + intVar + ", "
                + "floatVar = " + floatVar + ", " + "doubleVar = " + doubleVar + ", " + "charVar = " + charVar + ", "
                + "boolVar = " + boolVar + ", " + "stringVar = " + stringVar);
    }
}
